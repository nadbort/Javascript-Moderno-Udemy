
//--> INCREMENTOS Y DECREMENTOS


let puntaje = 5;

//--> Incrementar en 1 usando ++ (Hay que llamarla para ver el incremento)

puntaje++;

console.log(puntaje);

//--> Incrementar la variable sin llamarla

++puntaje;

console.log(puntaje);

//-->> Decrementar en 1 usando --

puntaje--;

console.log(puntaje);

//--> Incrementar de 2 en dos o de 3 en 3 o mas

puntaje += 3;

console.log(puntaje);

//--> Decrementar de 2 en dos o de 3 en 3 o mas

puntaje -= 5;

console.log(puntaje);